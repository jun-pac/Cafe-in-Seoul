let lang = (typeof localStorage !== 'undefined' && localStorage.getItem('lang')) || 'ko';
const listeners = [];

export function getLang() { return lang; }
export function t(key) { return (DICT[lang] && DICT[lang][key]) ?? DICT.ko[key] ?? key; }
export function onLangChange(cb) { listeners.push(cb); }
export function setLang(l) {
  lang = DICT[l] ? l : 'ko';
  try { localStorage.setItem('lang', lang); } catch { /* ignore */ }
  if (typeof document !== 'undefined') document.documentElement.lang = lang;
  listeners.forEach((cb) => cb(lang));
}
// apply to static elements: <x data-i18n="key"> textContent, [data-i18n-ph] placeholder
export function applyStaticI18n(root = document) {
  root.querySelectorAll('[data-i18n]').forEach((el) => { el.textContent = t(el.getAttribute('data-i18n')); });
  root.querySelectorAll('[data-i18n-ph]').forEach((el) => { el.placeholder = t(el.getAttribute('data-i18n-ph')); });
  // resolve tooltips (so filter controls explain each category to the user too)
  root.querySelectorAll('[data-tip-i18n]').forEach((el) => { el.setAttribute('data-tip', t(el.getAttribute('data-tip-i18n'))); });
}
